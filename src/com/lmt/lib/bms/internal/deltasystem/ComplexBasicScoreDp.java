	/** {@inheritDoc} */
	@Override
	protected boolean testNote(BeMusicNoteType type) {
		// DPでは地雷オブジェを視覚効果ありとして判定しない。
		// DPは同時押し数が増えるごとに基本評価点が爆増するため、地雷オブジェを視覚効果ありとすると
		// 最終評価点が暴騰し、意図しないレベルの高レーティング値を出力してしまう。
		return type.hasVisualEffect() && (type != BeMusicNoteType.MINE);
	}
